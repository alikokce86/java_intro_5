package practice.variablesPractices;

public class BooleanAndCharPractice {
    public static void main(String[] args) {

        boolean negative = false;
        boolean positive = true;

        System.out.println("Negative = " + negative);
        System.out.println("Negative = \"" + negative + "\"");

        System.out.println("Positive = " + positive);
        System.out.println("Positive = \"" + positive + "\"");

        /////////////////////////////////////////////////////

        // Expected output : Ali
        char myFirstChar = 'A';
        char mySecondChar = 'l';
        char myThirdChar = 'i';

        System.out.print(myFirstChar);
        System.out.print(mySecondChar);
        System.out.print(myThirdChar);
        ///////////////////////////////////////////////////
        System.out.println();

        char firstLetter = 'C';
        char secondLetter = 'a';
        char thirdLetter = 't';

        System.out.print(firstLetter);
        System.out.print(secondLetter);
        System.out.print(thirdLetter);

        ///////////////////////////////////////////////////
        System.out.println();

        char letter1 = 'D';
        char letter2 = 'o';
        char letter3 = 'g';

        System.out.print("\"" + letter1);
        System.out.print(letter2);
        System.out.print(letter3 + "\"");
    }
}
